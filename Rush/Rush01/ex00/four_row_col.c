#include <unistd.h>

void	print_row_col(char *sol)
{
	int	i;

	i = 1;
	while (sol[i - 1] != '\0')
	{
		write(1, &sol[i - 1],1);
		if (i % 4 == 0)
		{
			write(1, "\n",1);
		}
		i++;
	}
}
